// Arithmetic Operators -> Numbers (minimum 2)
/*
1. * -> 
2. / -> Quotient
3. % -> Remainder
4. + -> 
5. - -> 
6. ** -> Power / Raise To
*/

let n1 = 11;
let n2 = 3;

console.log(`Multiplication -> ${n1} * ${n2} = ${n1 * n2}`);
console.log(`Division (Decimal Quotient) -> ${n1} / ${n2} = ${n1 / n2}`);
console.log(`Modulus (Remainder) -> ${n1} % ${n2} = ${n1 % n2}`);
console.log(`Double Multiply (Power) -> ${n1} ** ${n2} = ${n1 ** n2}`);

console.log(5 + 2 * 8 / 2 * 3);
console.log((5 + 2) * 8 / (2 * 3));